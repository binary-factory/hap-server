export enum CharacteristicUnit {
    /** The unit is only "degrees Celsius". */
    Celsius = 'celsius',

        /** The unit is in percentage "%". */
    Percentage = 'percentage',

        /** The unit is in arc degrees. */
    Arcdegrees = 'arcdegrees',

        /** The unit is in lux. */
    Lux = 'lux',

        /** The unit is in seconds. */
    Seconds = 'seconds'
}